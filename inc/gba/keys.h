#ifndef GBA_HEAD_KEYS
#define GBA_HEAD_KEYS

#define KEY_A 0x0001 // A button.
#define KEY_B 0x0002 // B button.
#define KEY_SELECT 0x0004 // Select button.
#define KEY_START 0x0008 // Start button.
#define KEY_RIGHT 0x0010 // Right button.
#define KEY_LEFT 0x0020 // Left button.
#define KEY_UP 0x0040 // Up button.
#define KEY_DOWN 0x0080 // Down button.
#define KEY_R 0x0100 // R button.
#define KEY_L 0x0200 // L button.
#define KEY_MASK 0x03FF // All keys mask.

#define KEY_BTNS 0x030F // Buttons mask.
#define KEY_DPAD 0x00F0 // D-Pad mask.

#endif

